// 지역코드 상수 맵 + 라벨 유틸 (SQL Dump와 값 1:1 동일)
// DB 컬럼: region(region_code, sido, gu_gun_1, gu_gun_2)

export type RegionEntry = {
  sido: string;
  guGun1: string;        // DB: gu_gun_1
  guGun2: string | null; // DB: gu_gun_2 (빈문자 또는 NULL)
};

/** 어떤 입력이 와도 '시군구 5자리' 문자열로 정규화 */
export function toSigungu5(code?: number | string | null): string {
  if (code === null || code === undefined) return "";
  const raw = String(code).trim();
  if (!raw) return "";
  const digits = raw.replace(/\D/g, "");
  if (!digits) return "";
  return digits.slice(0, 5); // 10자리 법정동 → 앞 5자리
}

/** 라벨 생성: gu_gun_2 → gu_gun_1 → sido */
export function regionLabel(code?: number | string | null, fallback?: string): string {
  const key = toSigungu5(code);
  if (!key) return fallback ?? "";
  const row = REGION_INDEX[key];
  if (!row) return fallback ?? `지역ID ${code}`;
  const g2 = (row.guGun2 ?? "").trim();
  const g1 = (row.guGun1 ?? "").trim();
  const sd = (row.sido ?? "").trim();
  return g2 || g1 || sd || fallback || `지역ID ${code}`;
}

/** 전체 경로 "시도 gu_gun_1 gu_gun_2" */
export function regionFullPath(code?: number | string | null, sep = " "): string {
  const key = toSigungu5(code);
  if (!key) return "";
  const row = REGION_INDEX[key];
  if (!row) return `지역ID ${code}`;
  const parts = [row.sido, row.guGun1, row.guGun2 ?? ""].map((v) => (v || "").trim()).filter(Boolean);
  return parts.join(sep);
}

/** 시도명으로 그룹화에 활용 */
export function getSido(code?: number | string | null): string {
  const key = toSigungu5(code);
  return REGION_INDEX[key]?.sido ?? "";
}

